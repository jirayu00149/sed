let currentIdx = 0;
let score = 0;
let canAnswer = true;

function shuffle(array) {
    return array.sort(() => Math.random() - 0.5);
}

function initQuiz() {
    shuffle(questions);
    showQuestion();
}

function showQuestion() {
    canAnswer = true;
    const qData = questions[currentIdx];
    document.getElementById('explanation').style.display = 'none';
    document.getElementById('question-text').innerText = qData.q;
    document.getElementById('question-counter').innerText = `ข้อที่ ${currentIdx + 1} / ${questions.length}`;
    
    // อัปเดต Progress Bar
    const percent = ((currentIdx + 1) / questions.length) * 100;
    document.getElementById('progress-bar').style.width = percent + '%';

    // สร้างปุ่มช้อยแบบสลับลำดับตัวเลือกในแต่ละข้อ
    const container = document.getElementById('options-container');
    container.innerHTML = '';
    
    // สร้าง Array ของ Index ตัวเลือกแล้วสลับ
    const optIndices = shuffle([0, 1, 2, 3]);
    
    optIndices.forEach(idx => {
        const btn = document.createElement('button');
        btn.className = 'option-btn';
        btn.innerText = qData.options[idx];
        btn.onclick = () => checkAnswer(idx, btn);
        container.appendChild(btn);
    });
}

function checkAnswer(chosenIdx, btn) {
    if (!canAnswer) return;
    canAnswer = false;
    
    const correctIdx = questions[currentIdx].correct;
    const explanation = document.getElementById('explanation');
    
    if (chosenIdx === correctIdx) {
        btn.classList.add('correct');
        score++;
    } else {
        btn.classList.add('wrong');
        // แสดงข้อที่ถูก
        Array.from(document.getElementsByClassName('option-btn')).forEach(b => {
            // ค้นหาปุ่มที่มีข้อความตรงกับข้อที่ถูก
            if(b.innerText === questions[currentIdx].options[correctIdx]) {
                b.classList.add('correct');
            }
        });
    }
    
    explanation.innerText = "คำอธิบาย: " + questions[currentIdx].exp;
    explanation.style.display = 'block';
}

function handleNext() {
    if (canAnswer) {
        alert("กรุณาเลือกคำตอบก่อนครับ!");
        return;
    }
    
    currentIdx++;
    if (currentIdx < questions.length) {
        showQuestion();
    } else {
        showResults();
    }
}

function showResults() {
    document.getElementById('quiz-content').classList.add('hidden');
    document.querySelector('.controls').classList.add('hidden');
    const screen = document.getElementById('result-screen');
    screen.classList.remove('hidden');
    document.getElementById('final-score').innerText = score;
    
    const msg = score >= (questions.length * 0.8) ? "เยี่ยมมาก Nawat! คุณพร้อมสอบแล้ว" : "พยายามอีกนิดนะ ทบทวนบทที่ 6-7 เพิ่มเติม";
    document.getElementById('evaluation-msg').innerText = msg;
}

